# Die Variable 'i' wird mit dem Wert -1 initialisiert.
i = -1

# Eine 'while'-Schleife startet, die läuft, solange 'i' kleiner als 10 ist.
while i < 10:
    # In jedem Durchlauf der Schleife wird ein Stern (*) gedruckt.
    print("*")
    # Der Wert von 'i' wird um 3 erhöht.
    i += 3

# Nachdem die 'while'-Schleife beendet ist (wenn 'i' nicht mehr kleiner als 10 ist),
# wird der 'else'-Block ausgeführt und ein weiterer Stern (*) gedruckt.
else:
    print("*")
