# Importiere das 'math' Modul, das mathematische Funktionen bereitstellt
import math

# Fordere den Benutzer auf, eine Zahl einzugeben. Die Eingabe wird in eine Ganzzahl umgewandelt und in 'val1' gespeichert
val1 = int(input("Enter a number: \n"))

# Berechne die Quadratwurzel der eingegebenen Zahl mit der 'sqrt' Funktion aus dem 'math' Modul und drucke das Ergebnis
print(math.sqrt(val1))
